# Restless

**Terminal-first API workbench for developers who move fast.**

Restless turns your terminal into a focused API command center.  
Probe. Inspect. Validate. Snapshot. Diff. Repeat.

---

## Philosophy

APIs are infrastructure.

Working with them should be:

- Fast
- Deterministic
- Scriptable
- CI-friendly
- Free of GUI friction

Restless is intentionally **core-first** and **terminal-native**.

